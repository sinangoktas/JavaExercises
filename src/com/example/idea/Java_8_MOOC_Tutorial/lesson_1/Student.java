package com.example.idea.Java_8_MOOC_Tutorial.lesson_1;

public class Student {

    int gradYear;
    double score;

    public Student(int gradYear, double score) {
        this.gradYear = gradYear;
        this.score = score;
    }

    public int getGradYear() {
        return gradYear;
    }

    public double getScore() {
        return score;
    }
}
